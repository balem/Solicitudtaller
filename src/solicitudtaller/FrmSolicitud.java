    private void cargarRubro(){
        try {
            String sql = "SELECT * FROM rubro order by descripcion ";
            z.snt = z.con.createStatement();
            z.rs = z.snt.executeQuery(sql);
            cboRubro.removeAllItems();
            while (z.rs.next()) {
                cboRubro.addItem(z.rs.getString("descripcion"));
            }
        } catch (SQLException ex) {
            Logger.getLogger(FrmSolicitud.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
    
    private void cargarArea(){
        try {
            String sql = "SELECT * FROM area order by descripcion ";
            z.snt = z.con.createStatement();
            z.rs = z.snt.executeQuery(sql);
            cboArea.removeAllItems();
            while (z.rs.next()) {
                cboArea.addItem(z.rs.getString("descripcion"));
            }
        } catch (SQLException ex) {
            Logger.getLogger(FrmSolicitud.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
    
