    private void cargarPrograma(){
        try {
            String sql = "SELECT * FROM programa order by descripcion ";
            z.snt = z.con.createStatement();
            z.rs = z.snt.executeQuery(sql);
            cboPrograma.removeAllItems();
            while (z.rs.next()) {
                cboPrograma.addItem(z.rs.getString("descripcion"));
            }
        } catch (SQLException ex) {
            Logger.getLogger(FrmSolicitud.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
